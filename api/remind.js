export default async function handler(req) {
  const botToken = process.env.TELEGRAM_BOT_TOKEN
  const chatId   = process.env.TELEGRAM_CHAT_ID

  if (!botToken || !chatId) {
    return new Response('Missing env vars: TELEGRAM_BOT_TOKEN and TELEGRAM_CHAT_ID required', { status: 500 })
  }

  const text = REMINDERS[Math.floor(Math.random() * REMINDERS.length)]

  const res = await fetch(`https://api.telegram.org/bot${botToken}/sendMessage`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ chat_id: chatId, text }),
  })

  const data = await res.json()

  if (!data.ok) {
    return new Response(JSON.stringify(data), { status: 500, headers: { 'Content-Type': 'application/json' } })
  }

  return new Response(JSON.stringify({ ok: true, sent: text }), {
    headers: { 'Content-Type': 'application/json' },
  })
}

